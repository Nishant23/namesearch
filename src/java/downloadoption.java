    /**
     * Handles the HTTP
     * <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        PrintWriter out=response.getWriter();
        HttpSession session=request.getSession(false);
        if(session!=null){
            if(request.getParameter("csv")!=null){
                String name=session.getAttribute("userid").toString();
                csv.fileUpload(name);
                request.getRequestDispatcher("csvDownload.html").forward(request, response);
            }
            else if(request.getParameter("txt")!=null){
                String name=session.getAttribute("userid").toString();
                txt.fileUpload(name);
                request.getRequestDispatcher("csvDownload.html").forward(request, response);
                
            }
            else if(request.getParameter("doc")!=null){
                String name=session.getAttribute("userid").toString();
                pdf.fileUpload(name);
                request.getRequestDispatcher("csvDownload.html").forward(request, response);
            }
        }else{
            out.print("<p>Please login first.</p>");
            request.getRequestDispatcher("index.html").include(request, response);
        }
    }

